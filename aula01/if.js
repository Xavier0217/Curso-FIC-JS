// if em js

let aluno = "Alfredo José"
let nota1 = 8.9
let nota2 = 3.4
const media = (nota1 + nota2) / 2

if (media >= 6) {
    // aprovado
    console.log(`Aluno ${aluno} está aprovado com media ${media}`)
} else {
    // reprovado
    console.log(`O ${aluno} está reprovado com media de ${media}`)
}